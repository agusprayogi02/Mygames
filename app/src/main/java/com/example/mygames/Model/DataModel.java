package com.example.mygames.Model;

public class DataModel {
    private int id,idgenre,idplatform;
    private String nama,deskripsi,genre,rilistahun,platform,pembuat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdgenre() {
        return idgenre;
    }

    public void setIdgenre(int idgenre) {
        this.idgenre = idgenre;
    }

    public int getIdplatform() {
        return idplatform;
    }

    public void setIdplatform(int idplatform) {
        this.idplatform = idplatform;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getRilistahun() {
        return rilistahun;
    }

    public void setRilistahun(String rilistahun) {
        this.rilistahun = rilistahun;
    }

    public String getPlatform() {
        return platform;
    }

    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public String getPembuat() {
        return pembuat;
    }

    public void setPembuat(String pembuat) {
        this.pembuat = pembuat;
    }
}
